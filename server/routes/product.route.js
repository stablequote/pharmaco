const express = require('express');
const router = express.Router();
const productController = require('../controllers/product.controller');

// Authentication routes
router.post('/add', productController.create);
router.get('/list-all', productController.listAllProducts);
router.get('/list/:id', productController.searchProduct);
router.put('update/:id', productController.updateProduct);
router.delete('delete/:id', productController.deleteSingle);
router.delete('/delete/multiple/', productController.deleteMultiple);

module.exports = router;